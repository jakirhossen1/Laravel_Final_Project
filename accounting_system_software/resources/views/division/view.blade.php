<script type="text/javascript">
function save(){
  
    var form=$('#save').get(0);
    $.ajax({
       
        url:'dv_save',
        method:'POST',
        data:new FormData(form),
        processData:false,
        contentType:false,
        success:function(){
         window.location.assign('dv');   
        }
       
        
    })
    
    
}
function delid(id){
    //alert(id)
    $('#del_id').val(id);
    //$('#del_id').val(id);// Return to Delete Modal input 
    
}
function upid(id,idc){
    $('#id').val(id);
    $('#u_division_name').val(idc);
   }
function update(){
    var id=$('#id').val();
    var division_name=$('#u_division_name').val();
    var token=$('#u_token').val()
    $.ajax({
        url:'dv_update',
        method:'POST',
        data:{id:id,division_name:division_name,_token:token},
        success:function(){
         window.location.assign('dv');   
        }
    })
    
    
}
function deletes(){
    var id=$('#del_id').val(); 
    var token=$('#_token').val();
   
    $.ajax({
        url:'dv_del',
        method:'POST',
        dataType:'html',
        data:{id:id,_token:token},
        success:function(){
             window.location.assign('dv');  
        }
    })
    
}


</script>
